	<h1>ACUERDO DE CONFIDENCIALIDAD Y NO DIVULGACIÓN DE LA INFORMACIÓN PARA EL INSTITUTO TECNOLÓGICO SUPERIOR CEMLAD
	</h1>
	<p>Intervienen en la celebración del presente <i>“ACUERDO DE CONFIDENCIALIDAD Y NO DIVULGACIÓN DE LA
			INFORMACIÓN”</i>, por
		una
		parte,<span>………………………………………………………</span>con cédula de ciudadanía Nro<span>……………………………</span>, en mi
		calidad de<span>………………………………</span>
		en adelante y para efectos del presente instrumento en representación del <b>ITS CEMLAD</b>en
		calidad
		de<span>……………………………</span>; y por otro lado <span>……………………</span>
		con cédula de ciudadanía Nro.<span>……………………</span> en mi
		calidad de<span>……………………</span> <b>(cargo)</b> perteneciente a<span>……………………</span>
		<b>(Institución)</b>, en adelante y para efectos del presente instrumento en calidad de
		<b>Receptor de la Información</b> quienes libre y voluntariamente celebran el presente acuerdo.
		<br>
		<br>
		Ambas partes reconocen recíprocamente su capacidad para obligarse, por lo que
		suscriben el presente Acuerdo de
		Confidencialidad y de No Divulgación de Información con base a las siguientes
		cláusulas.
	</p>
	<br>
	<h2>CLÁUSULA PRIMERA. - ANTECEDENTES:</h2>
	<p>En virtud de lo establecido en el numeral 19 del artículo 66 de la Norma Suprema se dispone: “Se reconoce y
		garantizará a las personas: (…) El derecho a la protección de datos de carácter personal, que incluye el acceso
		y la
		decisión sobre información y datos de este carácter, así como su correspondiente protección. La recolección,
		archivo, procesamiento, distribución o difusión de estos datos o información requerirán la autorización del
		titular
		o el mandato de la Ley”;
		<br>
		<br>
		El artículo 178 del Código Orgánico Integral Penal establece: “La persona que, sin contar con el consentimiento
		o la
		autorización legal, acceda, intercepte, examine, retenga, grabe, reproduzca, difunda o publique datos
		personales,
		mensajes de datos, voz, audio y vídeo, objetos postales, información contenida en soportes informáticos,
		comunicaciones privadas o reservadas de otra persona por cualquier medio, será sancionada con pena privativa de
		libertad de uno a tres años…”;
		<br>
		<br>
		El artículo 190 ibídem señala: “La persona que utilice fraudulentamente un sistema informático o redes
		electrónicas
		y de telecomunicaciones para facilitar la apropiación de un bien ajeno o que procure la transferencia no
		consentida
		de bienes, valores o derechos en perjuicio de esta o de una tercera, en beneficio suyo o de otra persona
		alterando,
		manipulando o modificando el funcionamiento de redes electrónicas, programas, sistemas informáticos, telemáticos
		y
		equipos terminales de telecomunicaciones, será sancionada con pena privativa de libertad de uno a tres años(…)”;
		<br>
		<br>
		El artículo 230 del Código Orgánico Integral Penal determina: “Será sancionada con pena privativa de libertad de
		tres a cinco años: (…) La persona que sin orden judicial previa, en provecho propio o de un tercero, intercepte,
		escuche, desvíe, grabe u observe, en cualquier forma un dato informático en su origen, destino o en el interior
		de
		un sistema informático, una señal o una transmisión de datos o señales con la finalidad de obtener información
		registrada o disponible. (…)”;
		<br>
		<br>
		La Ley de Comercio Electrónico, Firmas Electrónicas y Mensajes de Datos, en los artículos 2 y 44,
		respectivamente,
		reconoce ante el Estado la validez jurídica de los mensajes de datos electrónicos, así como el valor y efecto
		jurídicos de cualquier actividad, transacción mercantil, financiera o de servicios que se realice con los mismos
		por
		medio de redes electrónicas;
		<br>
		<br>
		La Carta Iberoamericana de Gobierno Electrónico, en la sección 24, recomienda a los gobiernos tomar en
		consideración
		la importancia de la interoperabilidad de las comunicaciones y servicios, así como disponer las medidas
		necesarias,
		para que todas las entidades públicas, cualquiera que sea su nivel y con independencia del respeto a su
		autonomía,
		establezcan sistemas que sean interoperables;
		<br>
		<br>
		La Ley del Sistema Nacional de Registro de Datos Públicos publicada en el Registro Oficial No. 162 de 31 de
		marzo de
		2010, en su artículo 4, cita: “Las instituciones del sector público y privado y las personas naturales que
		actualmente o en el futuro administren bases o registros de datos públicos, son responsables de la integridad,
		protección y control de los registros y bases de datos a su cargo. Dichas instituciones responderán por la
		veracidad, autenticidad, custodia y debida conservación de los registros. La responsabilidad sobre la veracidad
		y
		autenticidad de los datos registrados, es exclusiva de la o el declarante cuando esta o este provee toda la
		información…”;
		<br>
		<br>
		El artículo 27 de la Ley ibídem establece: “Las Registradoras o Registradores y máximas autoridades, a quienes
		se
		autoriza el manejo de las licencias para el acceso a los registros de datos utilizados por la ley, serán las o
		los
		responsables directos administrativa, civil y penalmente por el mal uso de las mismas”;
		<br>
		<br>
		Bajo este marco regulatorio, la información que se dispone en los servicios institucionales se clasifica como
		reservada y/o confidencial, a tal efecto se acuerda suscribir el presente “Acuerdo de Confidencialidad y No
		Divulgación de la Información” entre la entidad que solicita el acceso a los servicios institucionales y la
		entidad
		proveedora del servicio con la finalidad de proteger la información que se consuma cuando ésta tenga el carácter
		de
		reservada y/o confidencial.
		<br>
		<br>
		Se garantizará la confidencialidad, integridad, disponibilidad, reserva y protección de los datos e información
		que
		se comparta e intercambie entre las entidades, de acuerdo a la normativa vigente.
	</p>
	<br>
	<h2>CLÁUSULA SEGUNDA. - OBJETO:</h2>
	<p>
		En virtud de los antecedentes expuestos, por medio del presente instrumento el <b>RECEPTOR DE LA INFORMACIÓN</b>
		se
		obliga
		expresamente a guardar sigilo, confidencialidad y reserva sobre el contenido de toda la información generada,
		verbal
		o escrita, que se comparta entre las partes.
		Además, el <b>RECEPTOR DE LA INFORMACIÓN</b> se compromete a hacer uso de la información, únicamente para las
		actividades
		relacionadas con las funciones que desempeña, conforme a las obligaciones y prohibiciones legales pertinentes.
	</p>
	<br>
	<h2>CLÁUSULA TERCERA. - DERECHOS Y OBLIGACIONES:</h2>
	<p>Son obligaciones de la Secretaría de Educación Superior, Ciencia, Tecnología e Innovación las siguientes:
	<ol>
		<li>Suministrará al <b>RECEPTOR DE LA INFORMACIÓN</b> el informe/la información que estime necesaria para el
			desarrollo
			del
			contrato y/o proyecto o giro de negocio.</li>
	</ol>
	Son deberes de quien haga las veces de <b>RECEPTOR DE LA INFORMACIÓN</b>:
	<ol>
		<li>Suministrará al <b>RECEPTOR DE LA INFORMACIÓN</b> el informe/la información que estime necesaria para el
			desarrollo
			del contrato y/o proyecto o giro de negocio.
		<li>Todo funcionario público de cualquier entidad pública y/o empleado de empresa privada que haga uso y
			tenga acceso a la información proporcionada por la <b>ITS CEMLAD</b>, deberá suscribir el presente
			instrumento.
		</li>
		<li>Mantener en forma estrictamente reservada y confidencial toda la información que por razón de su
			competencia tendrá acceso, por lo tanto, se obliga a abstenerse de usar, disponer, divulgar y/o publicar por
			cualquier medio, oral, escrito, y/o tecnológico y en general, aprovecharse de ella en cualquier otra forma
			para efectos ajenos a los intereses de la Institución a la cual pertenece.</li>
		<li>Utilizar la información suministrada por la <b>ITS CEMLAD</b>, únicamente para los fines acordados por las
			partes.</li>
	</ol>
	No realizar copia o duplicado alguno de la información mencionada en este acuerdo sin la autorización previa y
	escrita de la otra parte; tampoco podrán divulgar dicha información a terceras personas sin que medie igualmente la
	respectiva autorización previa y escrita de la otra parte. Se excluye de esta obligación la información que sea de
	dominio público o que sea del conocimiento previo de la <b>ITS CEMLAD</b>, sin constituir discreción de la
	información en
	los términos del presente acuerdo y, cuya revelación no cause agravio o perjuicio alguno a su titular.
	</p>
	<h2>CLÁUSULA CUARTA. - PATRÓN DE CONDUCTA, IMPLICACIONES DE LA RECEPCIÓN DE LA INFORMACIÓN Y RESPONSABILIDAD</h2>
	<p>
		Las partes actuarán con responsabilidad en el buen uso de la información, lo que supone entre otros deberes, el
		de
		limitar la divulgación autorizada al menor número de personas, y el de tomar las medidas idóneas y eficaces para
		evitar el tráfico y fuga indebida de la información, así como su uso por fuera de los límites de este convenio.
		<br>
		<br>
		El incumplimiento del deber de reserva establecido en la Cláusula Cuarta de este acuerdo, constituye violación
		de
		secreto y justa causa de terminación unilateral de la relación, sin desmedro de las indemnizaciones (sólo para
		proveedores) legales correspondientes.
		<br>
		<br>
		El <b>RECEPTOR DE LA INFORMACIÓN</b> reconoce que la información confidencial a la que se refiere el presente
		acuerdo
		posee
		una valoración en imagen institucional y su indebida divulgación o utilización causa un perjuicio.
	</p>
	<br>
	<h2>CLÁUSULA QUINTA. - MATERIALES:</h2>
	<p>Todos los materiales como, documentos, actas de reunión, fichas de consumo, entre otras que son entregadas al
		<b>RECEPTOR DE LA INFORMACIÓN</b> por parte de la <b>ITS CEMLAD</b> se considera como información confidencial y
		se debe
		guardar
		absoluta reserva de la misma.
	</p>
	<br>
	<h2>CLÁUSULA SEXTA. - SANCIONES:</h2>
	<p>Para la aplicación de sanciones se tomará en cuenta lo establecido en la Constitución de la República del
		Ecuador,
		la Ley Orgánica de Transparencia y Acceso a la Información Pública, Ley Orgánica del Sistema Nacional de
		Registro de
		Datos Púbicos y demás normativa aplicable; sin perjuicio de las acciones civiles y penales que procedan en cada
		caso.
		<br>
		<br>
		Los funcionarios públicos que incumplieren las estipulaciones de este instrumento, podrán ser sancionados por la
		máxima autoridad de la entidad en la que prestan sus servicios, de conformidad con lo determinado en la Ley
		Orgánica
		del Servicio Público y su Reglamento.
	</p>
	<br>
	<h2>CLÁUSULA SÉPTIMA. -:</h2>
	<p> En los casos en que la información sea calificada como “Reservada” o “Confidencial” por mandato legal, las
		entidades
		que suscriben el presente acuerdo evaluarán la pertinencia de entregar o no dicha información.</p>
	<br>
	<h2>CLÁUSULA OCTAVA. - DOCUMENTOS HABILITANTES:</h2>
	<br>
	<p>Para la suscripción del presente instrumento, las partes presentarán las respectivas delegaciones o nombramientos
		que les permita actuar en representación de las entidades intervinientes.</p>
	<br>
	<h2>CLÁUSULA NOVENA. - VIGENCIA:</h2>
	<br>
	<p>El presente instrumento tendrá una vigencia de cinco años a partir de la fecha de suscripción.</p>
	<br>
	<h2>CLÁUSULA DÉCIMA - ACUERDO TOTAL:</h2>
	<br>
	<p>Este acuerdo incluye el total entendimiento entre las partes con relación a la materia de la cual se trata este
		documento. Cualquier añadidura o modificación a este acuerdo deberá ser hecha por escrito y firmada por ambas
		partes.</p>
	<br>
