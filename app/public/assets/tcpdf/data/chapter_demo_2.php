	<br>
	<p>En el evento de que se produzca el incumplimiento de alguna de las cláusulas estipuladas en el presente acuerdo,
		la
		parte afectada, notificará del incumplimiento a la máxima autoridad de la institución involucrada, sin perjuicio
		de
		las acciones y sanciones previstas en la normativa vigente.
